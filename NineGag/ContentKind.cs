
namespace NineGag
{
    /// <summary>
    /// Represents an enumeration for the different kinds of content.
    /// </summary>
    public enum ContentKind
    {
        /// <summary>
        /// The kind of content is unknown.
        /// </summary>
        Unknown,

        /// <summary>
        /// The content is an MP4 video.
        /// </summary>
        Mp4,

        /// <summary>
        /// The content is a WebM video.
        /// </summary>
        WebM,

        /// <summary>
        /// The content is a JPEG image.
        /// </summary>
        Jpeg
    }
}