
namespace NineGag
{
    /// <summary>
    /// Represents an enumeration for the different sub-sections of 9GAG.
    /// </summary>
    public enum SubSection
    {
        /// <summary>
        /// The hot sub-section, which contains posts that have a lot of upvotes and are popular among the 9GAG users.
        /// </summary>
        Hot,

        /// <summary>
        /// The fresh sub-section, which contains posts that do not have a lot of upvotes, yet, and have only recently been uploaded.
        /// </summary>
        Fresh
    }
}